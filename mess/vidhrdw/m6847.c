void m6847_get_bordercolor_rgb(int *red, int *green, int *blue)
{
	switch(m6847_get_bordercolor()) {
	case M6847_BORDERCOLOR_BLACK:
		*red = 0;
		*green = 0;
		*blue = 0;
		break;

	case M6847_BORDERCOLOR_GREEN:
		*red = 0;
		*green = 255;
		*blue = 0;
		break;

	case M6847_BORDERCOLOR_WHITE:
		*red = 255;
		*green = 255;
		*blue = 255;
		break;

	case M6847_BORDERCOLOR_ORANGE:
		*red = 255;
		*green = 128;
		*blue = 0;
		break;
	}
}

